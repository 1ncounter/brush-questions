/**
 * @param {number} n
 * @return {number}
 */
var climbStairs = function (n) {
  // 11 22 33 43
  // f(x) = f(x - 1) + f(x - 2)
  if (n < 3) return n;

  let dp = [];
  dp[0] = 0;
  dp[1] = 1;
  dp[2] = 2;

  for (let i = 3; i <= n; i++) {
    dp[i] = dp[i - 1] + dp[i - 2];
  }

  return dp[n];
};
